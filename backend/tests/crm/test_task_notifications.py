def test_check_reminders_ignores_terminal_tasks(app_client, owner_headers, owner_user):
    task = app_client.post(
        "/api/v1/crm/tasks",
        headers=owner_headers,
        json={"title": "Done and overdue", "assigned_to": str(owner_user.id), "due_date": "2020-01-01T00:00:00Z"},
    ).json()
    app_client.post(f"/api/v1/crm/tasks/{task['id']}/status", headers=owner_headers, json={"status": "done"})

    resp = app_client.post("/api/v1/crm/task-notifications/check", headers=owner_headers)
    assert resp.json()["items"] == []


def test_mark_notification_read(app_client, owner_headers, owner_user):
    app_client.post(
        "/api/v1/crm/tasks",
        headers=owner_headers,
        json={"title": "Read me", "assigned_to": str(owner_user.id), "due_date": "2020-01-01T00:00:00Z"},
    )
    created = app_client.post("/api/v1/crm/task-notifications/check", headers=owner_headers).json()["items"]
    notification_id = created[0]["id"]

    resp = app_client.post(f"/api/v1/crm/task-notifications/{notification_id}/read", headers=owner_headers)
    assert resp.status_code == 200
    assert resp.json()["read_at"] is not None

    unread = app_client.get(
        "/api/v1/crm/task-notifications", headers=owner_headers, params={"unread_only": True}
    ).json()["items"]
    assert notification_id not in [n["id"] for n in unread]
